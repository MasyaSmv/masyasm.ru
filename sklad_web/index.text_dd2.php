                    <p><b>Затем те, у которых есть длинное окончание:</b></p>
                    <p class="gray_fon">
                        ВВГ 3Х1,5 ГОСТ<br>
                        ВВГ 3Х1,5 ГОСТ ЧЕРН<br>
                        ВВГ 3Х1,5 ГОСТ ЧЕРН КОЛЬЧУГА<br>
                    </p>
                    <p><b>Если Вы хотите, чтобы именно Ваша позиция шла выше остальных старайтесь придерживаться вот такого варианта отображения складской справки:</b></p>
                    <p class="gray_fon">МАРКА_РАЗМЕР</p>
                    <p>Примечания, номера ГОСТов и ТУ, длины, артикулы и прочее мы стараемся удалять, чтобы выдача в поиске была чище и пользователю было проще найти нужную марку и длину.</p>
